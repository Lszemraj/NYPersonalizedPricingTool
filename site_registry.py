CATEGORY_LABELS: dict[str, str] = {
    CAT_FOOD_DEL: "Food delivery",
    CAT_GROCERY: "Grocery / retail",
    CAT_TRAVEL: "Travel / lodging / flights",
    CAT_TICKET: "Ticketing",
}


@dataclass(frozen=True)
class SiteProfile:
    platform_name: str
    category: str
    category_slug: str
    strategy_kind: StrategyKind
    homepage_url: str
    target_urls: tuple[str, ...]
    likely_entry_points: tuple[str, ...]
    likely_price_page_types: str
    requires_login: bool
    address_required: bool
    search_required: bool
    cart_supported: bool
    notes: str
    enabled: bool
    pilot_priority: bool


def _sp(
    name: str,
    category_slug: str,
    strat: StrategyKind,
    home: str,
    targets: tuple[str, ...],
    *,
    pilot: bool,
    entry: tuple[str, ...] = ("homepage",),
    price_types: str = "homepage",
    login: bool = False,
    addr: bool = False,
    search: bool = False,
    cart: bool = True,
    notes: str = "",
    enabled: bool = True,
) -> SiteProfile:
    return SiteProfile(
        platform_name=name,
        category=CATEGORY_LABELS[category_slug],
        category_slug=category_slug,
        strategy_kind=strat,
        homepage_url=home,
        target_urls=targets,
        likely_entry_points=entry,
        likely_price_page_types=price_types,
        requires_login=login,
        address_required=addr,
        search_required=search,
        cart_supported=cart,
        notes=notes,
        enabled=enabled,
        pilot_priority=pilot,
    )


