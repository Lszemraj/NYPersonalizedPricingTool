def normalize_category_filter(s: str) -> str | None:
    s = s.strip().lower().replace(" ", "_").replace("-", "_")
    aliases: dict[str, str] = {
        "food": CAT_FOOD_DEL,
        "food_delivery": CAT_FOOD_DEL,
        "delivery": CAT_FOOD_DEL,
        "grocery": CAT_GROCERY,
        "grocery_retail": CAT_GROCERY,
        "retail": CAT_GROCERY,
        "travel": CAT_TRAVEL,
        "lodging": CAT_TRAVEL,
        "flights": CAT_TRAVEL,
        "ticket": CAT_TICKET,
        "ticketing": CAT_TICKET,
        "tickets": CAT_TICKET,
    }
    if s in CATEGORY_LABELS:
        return s
    return aliases.get(s)
